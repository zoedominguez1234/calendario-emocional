function guardarDatos() {
  const nombre = document.getElementById("nombre").value.trim();
  const correo = document.getElementById("correo").value.trim();
  const grupo = document.getElementById("grupo").value.trim();

  if (!nombre || !correo || !grupo) {
    alert("Por favor, llena todos los campos antes de continuar.");
    return;
  }

  localStorage.setItem("usuario", JSON.stringify({ nombre, correo, grupo }));

  document.getElementById("registro").style.display = "none";
  document.getElementById("arbol").style.display = "flex";
}

function mostrarMes(mes) {
  document.getElementById("arbol").style.display = "none";
  const info = document.getElementById("infoMes");
  info.style.display = "block";
  document.getElementById("tituloMes").innerText = meses[mes].titulo;
  document.getElementById("descripcionMes").innerText = meses[mes].descripcion;
  document.getElementById("imagenMes").src = mes + ".jpg";
}

function volverArbol() {
  document.getElementById("infoMes").style.display = "none";
  document.getElementById("arbol").style.display = "flex";
}

function guardarSugerencia() {
  const texto = document.getElementById("sugerencia").value;
  if (texto.trim() === "") {
    alert("Escribe una sugerencia primero.");
    return;
  }
  alert("Gracias por tu sugerencia: " + texto);
  document.getElementById("sugerencia").value = "";
}


