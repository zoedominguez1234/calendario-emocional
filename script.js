// -------------------- FUNCIÓN PARA MOSTRAR LOS DATOS DEL MES --------------------
function mostrarMes(mes) {
    const contenedor = document.getElementById("contenidoMes");
    const tituloMes = document.getElementById("tituloMes");
    const descripcionMes = document.getElementById("descripcionMes");
  
    if (meses[mes]) {
      tituloMes.textContent = meses[mes].titulo;
      descripcionMes.textContent = meses[mes].descripcion;
      contenedor.classList.add("mostrar");
    } else {
      tituloMes.textContent = "Mes no encontrado";
      descripcionMes.textContent = "";
    }
  }
  
